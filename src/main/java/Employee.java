public class Employee
{
    private int id;
    private String fullName;

    public Employee()
    {

    }

    public int getId()
    {
        return id;
    }

    public void setId(int newId)
    {
        this.id = newId;
    }

    public String getFullName()
    {
        return fullName;
    }

    public void setFullName(String newFullName)
    {
        this.fullName = newFullName;
    }
}